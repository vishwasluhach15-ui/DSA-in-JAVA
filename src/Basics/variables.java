package Basics;

public class variables {
    static void main() {
        int n = 120 ;// initialize and declare
        System.out.println(n);//println => print and go to next line
        int x;//initialize
        x= 109;//declared
        System.out.print(x);//print => only print and do not enter next line
        System.out.println(" ");// this is also work like enter or next line

        System.out.println(x+" Vishwas");// int + string => append
        System.out.println("Hi i am Vishu\tI study in uiet rohtak  ");// t=> tap ka space approx 4 space bar
        System.out.print("This is my github repository\nForgive me for any incontinence ");// \n=> also for next line
//        we can also update any variable again
        x =20;
        System.out.println("\n"+x);//successfully updated variable
    }
}
